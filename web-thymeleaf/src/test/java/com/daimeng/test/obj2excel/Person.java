package com.daimeng.test.obj2excel;

public class Person {

	private int id;
	private String no;
	private String name;
	private int age;
	private int sex;
	private String address;
	private int tecYear;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getTecYear() {
		return tecYear;
	}
	public void setTecYear(int tecYear) {
		this.tecYear = tecYear;
	}
	
}
